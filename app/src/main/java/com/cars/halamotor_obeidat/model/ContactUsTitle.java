package com.cars.halamotor_obeidat.model;

public class ContactUsTitle {
    String title,titleS;

    public ContactUsTitle(String title, String titleS) {
        this.title = title;
        this.titleS = titleS;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTitleS() {
        return titleS;
    }

    public void setTitleS(String titleS) {
        this.titleS = titleS;
    }
}
